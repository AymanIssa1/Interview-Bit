class Solution:
    # @param A : list of integers
    # @param B : integer
    # @return an integer
    def removeElement(self, arr, element):
        length = 0
        for i in range(0, len(arr)):
            if arr[i] != element:
                arr[length] = arr[i]
                length += 1

        del arr[length:]
        return length


